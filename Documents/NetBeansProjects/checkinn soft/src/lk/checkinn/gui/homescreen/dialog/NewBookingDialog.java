    private void insertBooking() {
        try {
            // Get values from booking and customer panels
            java.util.Date checkin = bookingDetailsPanel.getCheckinDate();
            java.util.Date checkout = bookingDetailsPanel.getCheckOutDate();
            int adults = bookingDetailsPanel.getAdultsCount();
            int kids = bookingDetailsPanel.getKidsCount();
            String roomType = bookingDetailsPanel.getSelectedRoomType();
            String roomName = bookingDetailsPanel.getSelectedRoomName();

            String firstName = customerDetailsPanel.getFirstName();
            String lastName = customerDetailsPanel.getLastName();
            String nic = customerDetailsPanel.getNIC();
            String houseNo = customerDetailsPanel.getHouseNo();
            String line1 = customerDetailsPanel.getLine1();
            String line2 = customerDetailsPanel.getLine2();
            String mobile = customerDetailsPanel.getMobile();
            String email = customerDetailsPanel.getEmail();

            // Validation checks
            if (checkin == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please select Check-in date.");
                return;
            }
            if (checkout == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please select Check-out date.");
                return;
            }
            if (checkout.before(checkin)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Check-out date cannot be before Check-in date.");
                return;
            }
            if (adults <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Number of adults must be at least 1.");
                return;
            }
            if (roomType == null || roomType.equals("Select Room Type")) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please select a Room Type.");
                return;
            }
            if (roomName == null || roomName.equals("Select Room Name")) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please select a Room Name.");
                return;
            }
            if (firstName == null || firstName.trim().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "First name cannot be empty.");
                return;
            }
            if (lastName == null || lastName.trim().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Last name cannot be empty.");
                return;
            }
            if (nic == null || nic.trim().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "NIC cannot be empty.");
                return;
            }
            if (houseNo == null || houseNo.trim().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "House number cannot be empty.");
                return;
            }
            if (line1 == null || line1.trim().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Address Line 1 cannot be empty.");
                return;
            }
            if (mobile == null || !isValidMobile(mobile)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Mobile number must be exactly 10 digits and numeric.");
                return;
            }
            if (email == null || email.trim().isEmpty() || !isValidEmail(email)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid email address.");
                return;
            }

            // Calculate duration in days (inclusive)
            long diffInMillis = checkout.getTime() - checkin.getTime();
            long durationDays = java.util.concurrent.TimeUnit.DAYS.convert(diffInMillis, java.util.concurrent.TimeUnit.MILLISECONDS) + 1;

            // Database connection
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/checkinn_db", "root", "@Jonathan123");
            Statement stmt = con.createStatement();

            // Get room_type_id from room_type table
            String getRoomTypeIdQuery = "SELECT id FROM room_type WHERE type = '" + roomType + "'";
            ResultSet rsType = stmt.executeQuery(getRoomTypeIdQuery);
            int roomTypeId = -1;
            if (rsType.next()) {
                roomTypeId = rsType.getInt("id");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Selected room type not found in database.");
                return;
            }
            rsType.close();

            // Get room_name_id from room_name table
            String getRoomNameIdQuery = "SELECT id FROM room_name WHERE name = '" + roomName + "'";
            ResultSet rsName = stmt.executeQuery(getRoomNameIdQuery);
            int roomNameId = -1;
            if (rsName.next()) {
                roomNameId = rsName.getInt("id");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Selected room name not found in database.");
                return;
            }
            rsName.close();

            // Get adult_rate and child_rate from rates table based on room_type_id
            String getRatesQuery = "SELECT adult_rate, child_rate FROM rates WHERE room_type_id = " + roomTypeId;
            ResultSet rsRates = stmt.executeQuery(getRatesQuery);
            double adultRate = 0;
            double childRate = 0;
            if (rsRates.next()) {
                adultRate = rsRates.getDouble("adult_rate");
                childRate = rsRates.getDouble("child_rate");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Rates not found for selected room type.");
                return;
            }
            rsRates.close();

            // Calculate total price
            double totalPrice = (adultRate * adults + childRate * kids) * durationDays;

            // Convert java.util.Date to java.sql.Date for SQL insert
            java.sql.Date sqlCheckin = new java.sql.Date(checkin.getTime());
            java.sql.Date sqlCheckout = new java.sql.Date(checkout.getTime());

            // Insert booking record query with total
            String insertSQL = "INSERT INTO booking (checkin, checkout, adults, kids, room_type_id, room_name_id, first_name, last_name, nic, house_no, line_1, line_2, mobile, email, total) VALUES ("
                    + "'" + sqlCheckin + "', "
                    + "'" + sqlCheckout + "', "
                    + adults + ", "
                    + kids + ", "
                    + roomTypeId + ", "
                    + roomNameId + ", "
                    + "'" + firstName + "', "
                    + "'" + lastName + "', "
                    + "'" + nic + "', "
                    + "'" + houseNo + "', "
                    + "'" + line1 + "', "
                    + "'" + line2 + "', "
                    + "'" + mobile + "', "
                    + "'" + email + "', "
                    + totalPrice
                    + ")";

            int rowsInserted = stmt.executeUpdate(insertSQL);
            
            if (rowsInserted > 0) {
            // Update room status to booked (2)
            String updateRoomSQL = "UPDATE `room_name` SET `booking_status_id` = 1 WHERE id = " + roomNameId;
            stmt.executeUpdate(updateRoomSQL);

            javax.swing.JOptionPane.showMessageDialog(this, "Booking successfully added! Total Price: " + totalPrice);
            jLabel3.setText("Total: " + totalPrice);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Failed to add booking.");
        }

            stmt.close();
            con.close();

        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

// Mobile validation method
    private boolean isValidMobile(String mobile) {
        if (mobile == null || mobile.trim().isEmpty()) {
            return false;
        }
        if (mobile.length() != 10) {
            return false;
        }
        for (char c : mobile.toCharArray()) {
            if (!Character.isDigit(c)) {
                return false;
            }
        }
        return true;
    }

// Simple email validation method
    private boolean isValidEmail(String email) {
        if (email == null) {
            return false;
        }
        String emailRegex = "^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$";
        return email.matches(emailRegex);
    }

