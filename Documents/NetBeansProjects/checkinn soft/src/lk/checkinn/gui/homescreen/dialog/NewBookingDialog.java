    private void updateTotal() {
        try {
            // Get selected room type from BookingDetails
            String roomType = bookingDetailsPanel.getSelectedRoomType();
            if (roomType == null || roomType.equals("Select Room Type")) {
                jLabel3.setText("Total: -");
                return;
            }

            // Get adults and kids count
            int adults = bookingDetailsPanel.getAdultsCount();
            int kids = bookingDetailsPanel.getKidsCount();

            if (adults < 0 || kids < 0) {
                jLabel3.setText("Total: -");
                return;
            }

            // Connect DB and fetch rates for room type
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/checkinn_db", "root", "@Jonathan123");
            Statement stmt = con.createStatement();

            // Get room_type_id for the selected room type
            String getRoomTypeIdQuery = "SELECT id FROM room_type WHERE type = '" + roomType + "'";
            ResultSet rsType = stmt.executeQuery(getRoomTypeIdQuery);
            int roomTypeId = -1;
            if (rsType.next()) {
                roomTypeId = rsType.getInt("id");
            } else {
                jLabel3.setText("Total: -");
                rsType.close();
                stmt.close();
                con.close();
                return;
            }
            rsType.close();

            // Get rates for this room type
            String getRatesQuery = "SELECT adult_rate, child_rate FROM rates WHERE room_type_id = " + roomTypeId;
            ResultSet rsRates = stmt.executeQuery(getRatesQuery);

            double adultRate = 0;
            double childRate = 0;

            if (rsRates.next()) {
                adultRate = rsRates.getDouble("adult_rate");
                childRate = rsRates.getDouble("child_rate");
            } else {
                jLabel3.setText("Total: -");
                rsRates.close();
                stmt.close();
                con.close();
                return;
            }

            rsRates.close();
            stmt.close();
            con.close();

            // Calculate total
            double total = (adults * adultRate) + (kids * childRate);
            jLabel3.setText(String.format("Total: %.2f", total));

        } catch (Exception e) {
            e.printStackTrace();
            jLabel3.setText("Total: Error");
        }
    }

