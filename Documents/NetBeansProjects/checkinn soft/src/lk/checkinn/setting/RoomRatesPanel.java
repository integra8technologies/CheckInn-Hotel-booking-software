    private void deleteBtn_roomratesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtn_roomratesActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a rate to delete.",
                    "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String roomType = (String) jTable1.getValueAt(selectedRow, 2);

        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete rates for room type '" + roomType + "'?",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/checkinn_db", "root", "@Jonathan123");
                Statement s = con.createStatement();

                ResultSet rs = s.executeQuery("SELECT `id` FROM `room_type` WHERE `type` = '" + roomType + "'");

                if (rs.next()) {
                    int roomTypeId = rs.getInt("id");

                    int rowsDeleted = s.executeUpdate("DELETE FROM `rates` WHERE `room_type_id` = '" + roomTypeId + "'");

                    if (rowsDeleted > 0) {
                        JOptionPane.showMessageDialog(this, "Rates deleted successfully.",
                                "Delete Success", JOptionPane.INFORMATION_MESSAGE);
                        loadRoomRates();
                    } else {
                        JOptionPane.showMessageDialog(this, "No rates found to delete.",
                                "Delete Failed", JOptionPane.ERROR_MESSAGE);
                    }
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_deleteBtn_roomratesActionPerformed


