    private void insertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertBtnActionPerformed
        // TODO add your handling code here:
        String selectedRoomType = (String) roomTypeComboBoxModel.getSelectedItem();
        String adultRate = adultRateField.getText();
        String childRate = childRateField.getText();

//        System.out.println(selectedRoomType);
//        System.out.println(adultRate);
//        System.out.println(childRate);
        if (selectedRoomType.equals("Select Room Type")) {
            JOptionPane.showMessageDialog(this, "Please select a room type before inserting.",
                    "Room Type Validation",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (adultRate.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the adult rate before inserting.",
                    "Rate Validation",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (childRate.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter child rates before inserting.",
                    "Rate Validation",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!adultRate.matches("\\d+(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid numeric value for Adult Rate.",
                    "Adult Rate Validation",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!childRate.matches("\\d+(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid numeric value for Child Rate.",
                    "Child Rate Validation",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            Double adultRateDouble = Double.parseDouble(adultRate);
            Double childRateDouble = Double.parseDouble(childRate);

//            System.out.println(selectedRoomType);
//            System.out.println(adultRateDouble);
//            System.out.println(childRateDouble);
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con1 = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/checkinn_db", "root", "@Jonathan123");
            Statement s1 = con1.createStatement();
            ResultSet rs1 = s1.executeQuery("SELECT `id` FROM `room_type` WHERE `type` = '" + selectedRoomType + "'");

            if (rs1.next()) {
                int roomTypeId = rs1.getInt("id");

                ResultSet rsCheck = s1.executeQuery("SELECT * FROM `rates` WHERE `room_type_id` = '" + roomTypeId + "'");
                if (rsCheck.next()) {
                    JOptionPane.showMessageDialog(this, "Rates already exist for this room type.\nPlease update instead of adding a new entry.",
                            "Duplicate Entry", JOptionPane.WARNING_MESSAGE);

                    roomTypeComboBoxModel.setSelectedIndex(0);
                    adultRateField.setText("");
                    childRateField.setText("");
                    return;
                }

                s1.executeUpdate("INSERT INTO `rates` (`adult_rate`,`child_rate`,`room_type_id`)"
                        + "VALUES ('" + adultRateDouble + "', '" + childRateDouble + "', '" + roomTypeId + "') ");
                
                roomTypeComboBoxModel.setSelectedIndex(0);
                adultRateField.setText("");
                childRateField.setText("");
                
                loadRoomRates();

                JOptionPane.showMessageDialog(this, "Rates inserted successfully.",
                        "Insert Success",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e1) {
            e1.printStackTrace();
            JOptionPane.showMessageDialog(this, "Please enter valid numeric values for rates.",
                    "Number Format Error",
                    JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_insertBtnActionPerformed

    private void loadRoomRates() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con3 = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/checkinn_db", "root", "@Jonathan123");
            Statement s3 = con3.createStatement();
            ResultSet rs3 = s3.executeQuery("SELECT * FROM `rates` INNER JOIN `room_type` ON `rates`.`room_type_id` = `room_type`.`id`");

            Vector<Vector> ratesBacket = new Vector();

            while (rs3.next()) {
                Vector<String> ratesTableRow = new Vector();
                ratesTableRow.add(rs3.getString("adult_rate"));
                ratesTableRow.add(rs3.getString("child_rate"));
                ratesTableRow.add(rs3.getString("room_type.type"));

                ratesBacket.add(ratesTableRow);
            }

            Vector<String> ratesTableColumns = new Vector();
            ratesTableColumns.add("Adult Rate");
            ratesTableColumns.add("Child Rate");
            ratesTableColumns.add("Room Type");

            DefaultTableModel dtm = new DefaultTableModel(ratesBacket, ratesTableColumns);
            jTable1.setModel(dtm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
